public class matemática {
    public static void main(String[] args) {
        int resto = 10 % 1; // Resultado: 0
        int soma = 10 + 2; // Resultado: 12
        int divisao = 10 / 2; // Resultado: 5
        int multiplicacao = 10 * 2; // Resultado: 20
        int subtracao = 10 - 2; // Resultado: 8


        System.out.println("o resto da divisao de 10/1 é = " + resto); 
        System.out.println("o resultado da divisao de 10/2 é = " + divisao);
        System.out.println("a soma de 10+2 é = " + soma);
        System.out.println("a multiplicacao de 10x2 é = " + multiplicacao);
        System.out.println("a subtracao de 10-2 é = " + subtracao);
    }

}